void	ft_rev_int_tab(int *tab, int size)
{
	int	t;
	int	i;
	int	h;

	i = 0;
	t = 0;
	h = size / 2;
	while (h-- > 0)
	{
		t = tab [i];
		tab [i++] = tab [size - 1];
		tab [size-- -1] = t;
	}
}
